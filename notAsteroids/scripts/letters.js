function drawCharacter(x, y, scale, color, letter) {
    var letter = Text[letter];
    ctx.strokeStyle = color;
    ctx.beginPath();
    for (const coords of letter) {
        if (coords.length <= 2 || coords[2] == 0) {
            ctx.lineTo(
                x + coords[0] * scale,
                y + coords[1] * scale
            );
        }
        else
        {
            ctx.moveTo(
                x + coords[0] * scale,
                y + coords[1] * scale
            );
        }
    }
    ctx.stroke();
}

function drawText(x, y, scale, color, text) {
    var charX = x;
    for (const char of text.toUpperCase()) {
        if (char != ' ') {
            drawCharacter(charX, y, scale, color, char);
        }
        if (".:".includes(char)) {
            charX += 0.6 * scale
        }
        else {
            charX += 1.4 * scale
        }
    }
}

